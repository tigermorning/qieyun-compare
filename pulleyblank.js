const TONES = { '平': '', '上': '̄', '去': '̂', '入': '' };

function 判斷(地位) {
  const initial = INITIALS[地位.母];
  if (!initial) return '';

  const rime = FINALS[地位.韻];
  if (!rime) return '';

  const division = rime[地位.等];
  if (!division) return '';

  const rounding = 地位.呼 === '合' ? '合' : '開';
  let final = division[rounding] || division[Object.keys(division)[0]] || '';

  if (!final) return '';

  const tone = TONES[地位.聲] ?? '';

  return initial + final + tone;
}

